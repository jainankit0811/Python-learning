# Create a list using []
a = [1, 2, 3, 4, 35]

# Print the list using print() function
print(a)

# Access using index using a[0],a[1],a[2]
print(a[1], a[2], a[3])

# Change the value of list using
a[0] = 98
print(a)

# We can create a list with item of different types
c = [45, "ankit", True, 6.9]
print(c)

# list slicing
friend = ['Harry', "tom", "rohan", "sam", 7.5, 6]
print(friend[0:6])
print(friend[-4:])
